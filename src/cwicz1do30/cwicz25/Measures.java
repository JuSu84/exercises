package cwicz1do30.cwicz25;

public class Measures {

    private final static double CUP = 250;
    private final static double SPOON = 15;
    private final static double TEASPOON = 5;


    public static double calculateCup(double a){
        return CUP * a;
    }
    public static double calculateSPOON(double a){
        return SPOON * a;
    }
    public static double calculateTEASPOON(double a){
        return TEASPOON * a;
    }


}
